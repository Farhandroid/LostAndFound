package tanvir.lostandfound.PojoClass;

public class LoggedUserInformation {

    String Name , UserName , EmailAddress , PhoneNumber ,IsUserUploadedProfilePicture,message;

    public String getName() {
        return Name;
    }

    public String getUserName() {
        return UserName;
    }

    public String getEmailAddress() {
        return EmailAddress;
    }

    public String getPhoneNumber() {
        return PhoneNumber;
    }

    public String getIsUserUploadedProfilePicture() {
        return IsUserUploadedProfilePicture;
    }

    public String getMessage() {
        return message;
    }
}
